## Install devtools
if(!require("devtools")){
  install.packages("devtools")
}
################################################################################

################################################################################
## Install BiocManager
if (!requireNamespace("BiocManager", quietly = TRUE))
    install.packages("BiocManager")
################################################################################

################################################################################
# install latest dada2 from BiocManager 
BiocManager::install("dada2")
################################################################################

################################################################################
# Check dada2 package version
message("dada2 package version:\n", packageVersion("dada2"))
################################################################################

################################################################################
# install DECIPHER from BiocManager
BiocManager::install("DECIPHER")
################################################################################

################################################################################
# Check DECIPHER package version
################################################################################
message("DECIPHER package version:\n", packageVersion("DECIPHER"))
